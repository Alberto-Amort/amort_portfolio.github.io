    <p>Due delle figure più importanti della lotta contro  la mafia furono i giudici Giovanni Falcone (quest'ultimo noto per aver inventato il Metodo Falcone, 
        che si basava sul prevedere movimenti della mafia attraverso le sue azioni finanziarie) e Paolo Borsellino. I due, putroppo, furono uccisi, rispettivamente, 
        durante la strage di Capaci il 23 maggio 1992 e la strage di Via d'Amelio il 19 liglio 1992</p>
</section>

<section class=processo>
    <img src="./maxiprocesso.jpeg">
    <div><p>
        Come suggerisce il nome, il maxiprocesso fu il più grande processo penale della storia mondiale: il solo processo di primo grado aveva ben 475 imputati
         (che poi scenderanno a 460), decine di diversi capi d’imputazione e oltre 200 avvocati difensori, e terminò con 360 condanne, 
         114 assoluzioni (di cui gran parte furono però annullate il 30 gennaio 1992 dalla Corte di cassazione), 19 ergastoli, 
         pene detentive per un totale di 2665 anni di reclusione e 11 miliardi di lire in pene pecuniarie. 
         Iniziato il 10 febbraio 1986, venne svolto nella cosiddetta aula bunker, dotata dei più avanzi sistemi di protezione 
         (persino quelli antimissilistici) e di un sistema informatico per l'archiviazione degli atti all'avanguardia. 
         Data, inoltre, l'enorme mole di imputati e atti giudiziari, il processo fu suddiviso in vari tronconi. 
         Questi furono il maxiprocesso bis, avviato il 21 aprile 1987, e il maxiprocesso ter, avviato a inizio del maggio del 1988, entrambi dalla durata di un anno.  
         La vendetta della mafia, ovviamente, non tardò ad arrivare, e fra le vittime dei suoi omicidi efferati ci furono i già menzionati Falcone e Borsellino.  
         Il maxiprocesso, comunque, inflisse una dura batosta all'organizzazione, distruggendo i falsi miti dell'inesistenza, della potenza e dell'impunibilità di Cosa Nostra

    </p></div>
</section>

