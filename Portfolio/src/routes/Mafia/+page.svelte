    <h2>Definizione</h2>
    <p>Con mafia (acronimo di "Mazzini autorizza furti, incendi, avvelenamenti", detta  anche Cosa Nostra) s' intende un organizzazione criminale con radici in Sicilia 
        (ma che opera anche nel resto dell'Italia) retta da violenza, omertà, riti d'iniziazione e miti fondativi.  
        Oggi il termine viene anche utilizzato per indicare una qualsiasi organizzazione segreta di persone dedite
         ad attività illecite che impone la propria volontà attraverso mezzi illegali e violenti, 
         per conseguire interessi a fini privati e di arricchimento illegale, 
        anche a danno degli interessi pubblici (Esempi: camorra, 'ndrangheta, la Sacra Corona Unita).</p>
    </div>
</section>
