    <div>
    <h2>Le Agende Rosse</h2>

    <p>
        Le agende rosse è un movimento fondato da Salvatore Borsellino, il fratello di Paolo, il  cui obbiettivo è incoraggiare la ricerca della verità dietro la strage di Via d'Amelio
        e sostenere tutti i rappresentanti dello Stato che, nella loro lotta contro la criminalità organizzata, mettono in ballo la propria stessa vita (per cui hanno creato le Scorte civiche,
        dedite a proteggere quel determinato magistrato, giornalista, poliziotto eccetera) ma anche di impedire che il ricordo dei grandi eroi della guerra antimafia venga corrotto o cancellato. 
        Questo gruppo prende il nome da un'agenda rossa su cui Paolo Borsellino aveva annotato contenuti dei suoi colloqui investigativi, che scomparve misteriosamente in seguito al suo assassinio. 
        Probabilmente è stata rubata ed ora è usata per ricattare coloro i cui nomi sono stati scritti nel libretto. 
        La scelta di questo simbolo rappresenta la volontà del movimento di far luce sui misteri ancori irrisolti sulla strage di Via Amelio
        e, in generale, su tutto ciò che riguarda la mafia.

    </p></div>
