<section class=processo>

    <img src="./pool.jpg" alt=pool>
    <p>Il pool antimafia è un gruppo di magistrati che fu uno dei principali avversari di Cosa Nostra. 
        Fu fondato da Rocco Chinnici, capo dell'Ufficio Istruzione , nel 1980 in modo che i vari magistrati al lavoro sul caso mafia, 
        che prima lavoravano in solitaria, potessero condividere le informazioni raccolte, permettendo così di avere un quadro più tridimensionale sul fenomeno mafioso. 
        Una delle caratteristiche principali di questa organizzazione era l'enorme legame che univa i vari membri. 
         All'interno della squadra, infatti, vigeva un clima di forte fiducia e di collaborazione reciproca e non ci furono mai scontri di particolare rilevanza 
         (eccetto uno fra il giudice Falcone e il resto del pool sulla somiglianza a livello giuridico fra il reato di associazione a delinquere e il reato di associazione di stampo mafioso, 
         che Falcone considerava identici). Inoltre, comunicavano le recenti scoperte soltanto fra di loro, per evitare eventuali talpe. 
         Grazie alle informazioni ottenute delle loro indagini è stato possibile avviare il famosissimo maxiprocesso .
    </p>
</section>

