<section class=P2>
  <div><h2>P2</h2>

 <img src="./P2.jpeg">
<p>La P2 (Propaganda Due) fu una loggia massonica che apparteneva al Grande Oriente d’Italia. Originariamente istituita nel 1877 con il nome di Propaganda massonica, 
  venne sciolta durante il fascismo per poi rinascere col nome di Propaganda Due dopo la Seconda guerra mondiale. Nel corso degli anni '60, quando era Gran Maestro Lucio Gelli, 
  la P2 cominciò a reclutare imprenditori (tra i più noti, c’era Silvio Berlusconi), giornalisti (tra questi, il direttore del Corriere della Sera Franco di Bella e Maurizio Costanzo), 
  uomini politici, funzionari statali, ufficiali dell’Esercito, dei Carabinieri, della Guardia di Finanza, banchieri (come Roberto Calvi e Michele Sindona), 
  manager (come l’amministratore delegato di Rizzoli Bruno Tassan Din) nel tentativo di controllare la vita pubblica del paese e stabilizzarne la politica. La scoperta, il 17 marzo 1981, 
  della lista degli affiliati alla P2 produsse uno dei più grandi scandali politici della storia italiana.
</p>
  </div>
</section>

<section>
  <div><h2>Aldo Moro</h2>
  <p>Aldo Moro è stato un politico e giurista italiano. Fu uno dei fondatori della Democrazia Cristiana e cinque  volte Presidente del Consiglio. 
    Fu uno dei principali promotori del compromesso storico proposto da Enrico Berlinguer, che aveva l'obbiettivo di avvicinare democristiani e comunisti, 
    portando alla nascita del terzo governo Andreotti. Fu rapito dalle Brigate Rosse il 16 marzo 1978,  con l'intenzione di usarlo come ostaggio per ottenere la liberazione di alcuni 
    loro compagni detenuti a Torino. La DC attuò subito la “linea della fermezza”, rifiutando di trattare con i terroristi, ma non riuscirono a localizzare i rapitori. 
    Alla fine, dopo 55 giorni di prigionia, Aldo Moro fu condannato a morte dal "tribunale del popolo". La sua morte ruppe il fragile accordo tra comunisti e democristiani, 
    causando la caduta del governo Andreotti
  </p></div>

